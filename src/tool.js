/**
 * 类型判断
 * ==========================
 */

// 判断是否是数字
var _is_number = function (param) {
    return typeof param === 'number';
};

// 判断是否是函数
var _is_function = function (param) {
    return typeof param === 'function';
};

// 判断是否是字符串
var _is_string = function (param) {
    return typeof param === 'string';
};
